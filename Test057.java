/* ○ 과제
	  
	  다음과 같은 내용이 출력될 수 있도록
	  반복문의 중첩 구문을 작성하여 프로그램을 구현한다.

	실행 예)
    *
   ***
  *****
 *******
*********
	
											*/
public class Test057
{
	public static void main(String[] args)
	{
		int n1=-1; 
		int n2=5;
		for (int a=1;a<=5 ;a++ )
		{

			n2--;
			for (int b=1;b<=n2 ;b++ )
			
				System.out.print(" ");
			
			n1+=2;
			for (int s=1;s<=n1;s++ )
				System.out.print("*");

			System.out.println();
		}




	}

}
